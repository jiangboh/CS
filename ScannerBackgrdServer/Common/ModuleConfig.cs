using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ScannerBackgrdServer.Common
{
    /// <summary>
    /// 保存用户配置
    /// </summary>
    class ModuleConfig
    {
        /// <summary>
        /// Ap在线检测时间。单位(秒)（如果在该时间内未收到AP的心跳消息，认为AP下线）
        /// </summary>
        private int apOnLineTime = 180;

        /// <summary>
        /// App在线检测时间。单位(秒)（如果在该时间内未收到AP的心跳消息，认为APp下线）
        /// </summary>
        private int appOnLineTime = 180;

        public int ApOnLineTime { get => apOnLineTime; set => apOnLineTime = value; }
        public int AppOnLineTime { get => appOnLineTime; set => appOnLineTime = value; }
    }
}
